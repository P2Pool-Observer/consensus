TEXT ·aes_single_round_internal(SB),NOSPLIT|NOFRAME,$0-24
    MOVQ dst+0(FP), AX
	MOVQ src+8(FP), BX
	MOVQ roundKey+16(FP), CX

	MOVAPD 0(BX), X0
	MOVUPD 0(CX), X1

	AESENC X1, X0

	MOVUPD X0, 0(AX)
	RET
